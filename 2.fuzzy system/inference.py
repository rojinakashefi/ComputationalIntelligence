def inference(chest_pain, blood_pressure, cholestrol,
              blood_sugar, ecg, heart_rate, exercise,
              old_peak, thallium, sex, age):
  sick_4, sick_3, sick_2, sick_1, healthy = (0, 0, 0, 0, 0)
  sick_4 = max(sick_4, min(age["very_old"], chest_pain["atypical_anginal"]))
  sick_4 = max(sick_4, min(heart_rate["high"], age["old"]))
  sick_3 = max(sick_3, min(sex["male"], heart_rate["medium"]))
  sick_2 = max(sick_2, min(sex["female"], heart_rate["medium"]))
  sick_3 = max(sick_3, min(chest_pain["non_anginal_pain"], blood_pressure["high"]))
  sick_2 = max(sick_2, min(chest_pain["typical_anginal"], heart_rate["medium"]))
  sick_3 = max(sick_3, min(blood_sugar["true"], age["mild"]))
  sick_2 = max(sick_2, min(blood_sugar["false"], blood_pressure["very_high"]))
  sick_1 = max(sick_1, max(chest_pain["asymptomatic"], age["very_old"]))
  sick_1 = max(sick_1, max(blood_pressure["high"], heart_rate["low"]))
  healthy = max(healthy, chest_pain["typical_anginal"])
  sick_1 = max(sick_1, chest_pain["atypical_anginal"])
  sick_2 = max(sick_2, chest_pain["non_anginal_pain"])
  sick_3 = max(sick_3, chest_pain["asymptomatic"])
  sick_4 = max(sick_4, chest_pain["asymptomatic"])
  sick_1 = max(sick_1, sex["female"])
  sick_2 = max(sick_2, sex["male"])
  healthy = max(healthy, blood_pressure["low"])
  sick_1 = max(sick_1, blood_pressure["medium"])
  sick_2 = max(sick_2, blood_pressure["high"])
  sick_3 = max(sick_3, blood_pressure["high"])
  sick_4 = max(sick_4, blood_pressure["very_high"])
  healthy = max(healthy, cholestrol['low'])
  sick_1 = max(sick_1, cholestrol['medium'])
  sick_2 = max(sick_2, cholestrol['high'])
  sick_3 = max(sick_3, cholestrol["high"])
  sick_4 = max(sick_4, cholestrol['very_high'])
  sick_2 = max(sick_2, blood_sugar['true'])
  healthy = max(healthy, ecg['normal'])
  sick_1 = max(sick_1, ecg['normal'])
  sick_2 = max(sick_2, ecg['abnormal'])
  sick_3 = max(sick_3, ecg["hypertrophy"])
  sick_4 = max(sick_4, ecg['hypertrophy'])
  healthy = max(healthy, heart_rate["low"])
  sick_1 = max(sick_1, heart_rate['medium'])
  sick_2 = max(sick_2, heart_rate['medium'])
  sick_3 = max(sick_3, heart_rate['high'])
  sick_4 = max(sick_4, heart_rate["high"])
  sick_2 = max(sick_2, exercise['true'])
  healthy = max(healthy, old_peak['low'])
  sick_1 = max(sick_1, old_peak['low'])
  sick_2 = max(sick_2, old_peak['terrible'])
  sick_3 = max(sick_3, old_peak["terrible"])
  sick_4 = max(sick_4, old_peak["risk"])
  healthy = max(healthy, thallium['normal'])
  sick_1 = max(sick_1, thallium["normal"])
  sick_2 = max(sick_2, thallium['medium'])
  sick_3 = max(sick_3, thallium["high"])
  sick_4 = max(sick_4, thallium['high'])
  healthy = max(healthy, age['young'])
  sick_1 = max(sick_1, age['mild'])
  sick_2 = max(sick_2, age['old'])
  sick_3 = max(sick_3, age["old"])
  sick_4 = max(sick_4, age["very_old"])
  return healthy, sick_1, sick_2, sick_3, sick_4
